/**
 * Resolve an incoming complaint string to its canonical slug.
 * Returns the input unchanged if no alias found.
 */
export function resolveComplaintAlias(raw: string): string {
  const key = raw.trim().toLowerCase().replace(/-/g, " ");
  return COMPLAINT_ALIASES[key] ?? key.replace(/\s+/g, "_");
}
