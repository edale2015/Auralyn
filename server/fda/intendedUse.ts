/**
 * FDA SaMD Intended Use Statement
 *
 * Required for 21 CFR Part 820 Quality System Regulation and FDA's
 * "Software as a Medical Device" (SaMD) Pre-submission framework.
 *
 * Per FDA guidance "Clinical Decision Support Software" (Sept 2022):
 *   - Must explicitly define intended use, user, patient population
 *   - Must state the device class and regulatory pathway
 *   - Must declare human-in-the-loop status
 *   - CDS that is NOT intended to replace clinical judgment is often
 *     exempt from device regulation (21st Century Cures Act, §3060)
 *
 * This file is the authoritative single source of truth for all
 * regulatory filings, 510(k) submissions, and De Novo requests.
 */

export type SaMDClass = "I" | "II" | "III";
export type RegPathway = "510k" | "DeNovo" | "PMA" | "Exempt";

export interface ClinicalScope {
  conditions:       string[];
  patientAges:      string;
  careSetting:      string;
  excludedUses:     string[];
}

export interface RegulatoryProfile {
  deviceClass:        SaMDClass;
  regulatoryPathway:  RegPathway;
  productCode?:       string;
  predicate?:         string;
  imdrf: {
    state:   string;   // IMDRF SaMD state of healthcare situation
    purpose: string;   // IMDRF SaMD intended purpose
    type:    string;   // IMDRF significance
  };
}

export interface IntendedUseStatement {
  productName:        string;
  version:            string;
  lastReviewed:       string;       // ISO date
  description:        string;
  intendedUser:       string;
  intendedPatients:   string;
  clinicalScope:      ClinicalScope;
  humanInLoop:        boolean;
  humanInLoopDetail:  string;
  notIntendedFor:     string[];
  regulatory:         RegulatoryProfile;
  safetyClassification: string;
  contraindications:  string[];
  warnings:           string[];
}

