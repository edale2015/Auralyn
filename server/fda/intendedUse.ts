/** Machine-readable summary for API and regulatory audit endpoints */
export function getIntendedUseSummary() {
  return {
    productName:       intendedUse.productName,
    version:           intendedUse.version,
    deviceClass:       intendedUse.regulatory.deviceClass,
    pathway:           intendedUse.regulatory.regulatoryPathway,
    humanInLoop:       intendedUse.humanInLoop,
    clinicalConditions: intendedUse.clinicalScope.conditions.length,
    intendedUser:      intendedUse.intendedUser,
    safetyClass:       intendedUse.safetyClassification,
  };
}
